package javaprogramweek6;

public class Pro15_Swap {
    public static void main(String[] args) {
        int x=10;
        int y=20;
        System.out.println("x="+x);
        System.out.println("Y="+y);
        int temp=x;
        x=y;
        y=temp;
        //Swapping
        System.out.println("After Swapping");
        System.out.println("x="+x);
        System.out.println("Y="+y);
    }


}
